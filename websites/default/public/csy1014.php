			<h1>CSY1014 Computer Systems</h1>

<p>This module provides basic knowledge of the hardware and software components that make up a computer system. Modern computer systems not only consist of just the computer, but also include a range of peripherals. The module gives an overview of all the important concepts in preparation for a deeper study of computer systems. </p>

<p>
